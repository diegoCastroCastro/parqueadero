package ec.ups.edu.appdis.g2.parqueadero.modelo;

public class Vehiculo {
	private int color;
	private int marca;
	private int placa;
	
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public int getMarca() {
		return marca;
	}
	public void setMarca(int marca) {
		this.marca = marca;
	}
	public int getPlaca() {
		return placa;
	}
	public void setPlaca(int placa) {
		this.placa = placa;
	}

}
